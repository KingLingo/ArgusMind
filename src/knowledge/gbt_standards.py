def get_gbt_mapping(vuln_type: str, language: str = "") -> str:
    """获取漏洞类型在指定语言下的 GB/T 条款映射。

    优先查找语言特定映射，回退到 default。
    """
    vuln_upper = vuln_type.upper()
    lang_map = VULN_GBT_FULL_MAP.get(vuln_upper)
    if not lang_map:
        return VULN_GBT_MAP.get(vuln_upper, "")
    if language and language.lower() in lang_map:
        return lang_map[language.lower()]
    return lang_map.get("default", VULN_GBT_MAP.get(vuln_upper, ""))


